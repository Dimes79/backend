// СЕВЕРО-ЕВРОПЕЙСКИЙ ГАЗОПРОВОД 2
const name = "Северо-Европейский Газопровод 2";

async function seed() {
    try {
        const project = await Project.findOne({ where: { name } });
        if (!project) {
            return null;
        }
        const pickets = getPickets();
        for (let i = 0; i < pickets.length; i += 1) {
            console.log([i + 1, pickets.length]);
            const picket = pickets[i];
            const data = {
                projectId: project.get("id"),
                name: picket.name.toString(),
            };

            // eslint-disable-next-line no-await-in-loop
            let model = await Picket.findOne({
                where: data,
            });
            if (model) {
                // eslint-disable-next-line no-continue
                continue;
            }

            model = new Picket({
                gps: {
                    lat: picket.gps[0],
                    long: picket.gps[1],
                },
                ...data,
            });
            // eslint-disable-next-line no-await-in-loop
            await model.save();
        }
    } catch (e) {
        console.log("error", e);
    }
}

